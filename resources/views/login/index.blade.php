    var Login = function() {

    var handleLogin = function() {

        $('.login-form').validate({
            errorElement: 'span', //default input error message container
            errorClass: 'help-block', // default input error message class
            focusInvalid: false, // do not focus the last invalid input
            rules: {
                username: {
                    required: true
                },
                password: {
                    required: true
                },
                remember: {
                    required: false
                }
            },

            messages: {
                username: {
                    required: "請務必輸入使用者帳號"
                },
                password: {
                    required: "請務必輸入使用者密碼"
                }
            },

            invalidHandler: function(event, validator) { //display error alert on form submit   
                $('.alert-danger', $('.login-form')).show();
            },

            highlight: function(element) { // hightlight error inputs
                $(element)
                    .closest('.form-group').addClass('has-error'); // set error class to the control group
            },

            success: function(label) {
                label.closest('.form-group').removeClass('has-error');
                label.remove();
            },

            errorPlacement: function(error, element) {
                error.insertAfter(element.closest('.input-icon'));
            },

            submitHandler: function(form) {
                console.log('form submit');
                $.ajax({
                    'type' : 'POST',
                    'url'  : '{{action('LoginController@login')}}',
                    'dataType' : 'json',
                    'data' : {
                        'username' : $('input[name="username"]').val(),
                        'password' : $('input[name="password"]').val(),
                        '_token' : '{{csrf_token()}}',
                    },
                    success : function(sendback){
                        if(sendback.auth == true){
                            $('#login_response_message').empty().html('認證成功，進行登入中...');
                            $('#login_response').removeClass('alert-danger').addClass('alert-success').show();
                            window.location.href ='{{session('next_url')}}';
                        }else{
                            $('#login_response_message').empty().html('認證失敗，請確定您的帳號密碼正確');
                            $('#login_response').removeClass('alert-success').addClass('alert-danger').show();
                        }
                    },
                    error : function(sendback){
                        console.log(sendback);
                    }
                });
            }
        });

        $('.login-form input').keypress(function(e) {
            if (e.which == 13) {
                if ($('.login-form').validate().form()) {
                    $('.login-form').submit(); //form validation success, call ajax form submit
                }
                return false;
            }
        });
    }

